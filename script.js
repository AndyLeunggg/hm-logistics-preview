function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("hm_lang", lang);
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  const titleKey = document.body.dataset.titleKey || "titleHome";
  if (translations[lang][titleKey]) {
    document.title = translations[lang][titleKey];
  }

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = translations[lang][node.dataset.i18n];
    if (value) {
      node.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const value = translations[lang][node.dataset.i18nPlaceholder];
    if (value) {
      node.setAttribute("placeholder", value);
    }
  });

  updateWhatsAppLinks();
}

function setActiveNavigation() {
  const page = document.body.dataset.page;
  document.querySelectorAll("[data-route]").forEach((link) => {
    if (link.dataset.route === page) {
      link.setAttribute("aria-current", "page");
    } else {
      link.removeAttribute("aria-current");
    }
  });
}

function animateCounter(node) {
  if (node.dataset.counted === "true") return;
  const target = Number(node.dataset.count);
  if (!Number.isFinite(target)) return;

  node.dataset.counted = "true";
  const suffix = node.dataset.suffix || "";
  const duration = 900;
  const start = Date.now();

  function tick() {
    const progress = Math.min((Date.now() - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    node.textContent = `${Math.round(target * eased)}${suffix}`;
    if (progress < 1) {
      requestAnimationFrame(tick);
    }
  }

  requestAnimationFrame(tick);
}

function setupMotion() {
  const motionTargets = document.querySelectorAll(".reveal-section");
  const counterTargets = document.querySelectorAll("[data-count]");

  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
    motionTargets.forEach((node) => node.classList.add("is-visible"));
    counterTargets.forEach(animateCounter);
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          entry.target.querySelectorAll?.("[data-count]").forEach(animateCounter);
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.16, rootMargin: "0px 0px -8% 0px" }
  );

  motionTargets.forEach((node) => observer.observe(node));

  const counterObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          animateCounter(entry.target);
          counterObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.5, rootMargin: "0px 0px -6% 0px" }
  );

  counterTargets.forEach((node) => counterObserver.observe(node));
}

